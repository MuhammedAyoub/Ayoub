#Slicing
Name='WELCOME';
print(Name)
print(Name[3])
print(Name[0:4])
print(Name[2:])
print(Name[:5])
print(Name[-1])
print(Name[:-1])
print(Name[-5:-2])

#length
print(len(Name))

#strip (unwanted space to clear)
word="  This is my Bike 2020   "
print(word.strip())

#upper & lower Case
print(Name.lower())
print(Name.upper())

#replace
print(Name.replace('C','K'))

print('my' in word)

